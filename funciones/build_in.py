numeros = (3,4,5,6,7,12)

# encontrar el numero mayor
print(max(numeros))
print('-----------------------')

#encontrar el numero menor
print(min(numeros))
print('-----------------------')

#redondeado a 3 decimales
print(round(12.334712, 3))
print('-----------------------')

